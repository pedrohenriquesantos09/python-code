#numeros para trabalho

refrigerante = 2000 #ml
consumo_crianca = 350 #ml
numero_crianca1 = 34
numero_crianca2 = 45

#cenario1

refrigerantes_necessarios1 = consumo_crianca * numero_crianca1 / refrigerante
print(f'Serão necessários para 34 crianças: {refrigerantes_necessarios1:.2f} garrafas de 2 litros')

#cenario2

refrigerantes_necessarios2 = consumo_crianca * numero_crianca2 / refrigerante
print(f'Serão necessários para 45 crianças: {refrigerantes_necessarios2:.2f} garrafas de 2 litros')


